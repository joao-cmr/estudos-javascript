// Operadores relacionais > >= < <= == != (! negação) not

let num1 = 10
let num2 = 5
let num3 = 10

console.log(num1 > num2) // true
console.log(num1 < num2) // false
console.log(num1 >= num3) // true
console.log(num1 <= num3) // true
console.log(num1 == num3) // true
console.log(!(num1 != num2)) // false
